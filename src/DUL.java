import java.util.Date;

public class DUL {
    String type;
    String seria;
    String number;
    String code;
    Date date;

    public void setType(String type) {
        this.type = type;
    }

    public void setSeria(String seria) {
        this.seria = seria;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}
